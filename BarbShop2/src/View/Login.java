    private void formComponentResized(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentResized
        // Obtém o tamanho atual do JFrame
        int frameWidth = getWidth();
        int frameHeight = getHeight();

        // Obtém a imagem atual do JLabel
        ImageIcon icon = (ImageIcon) JLabelFundo.getIcon();
        Image img = icon.getImage();

        // Obtém as dimensões da imagem original
        int imgWidth = img.getWidth(null);
        int imgHeight = img.getHeight(null);

        // Calcula as proporções para redimensionar a imagem mantendo a proporção original
        double scaleFactor = Math.min((double) frameWidth / imgWidth, (double) frameHeight / imgHeight);
        int newWidth = (int) (imgWidth * scaleFactor);
        int newHeight = (int) (imgHeight * scaleFactor);

        // Redimensiona a imagem mantendo a proporção
        Image newImg = img.getScaledInstance(newWidth, newHeight, Image.SCALE_FAST);

        // Calcula o deslocamento necessário para centralizar a imagem no JFrame
        int x = (frameWidth - newWidth) / 2;
        int y = (frameHeight - newHeight) / 2;

        // Define a nova imagem redimensionada e centralizada no JLabel
        JLabelFundo.setIcon(new ImageIcon(newImg));
        JLabelFundo.setBounds(x, y, newWidth, newHeight);
    }//GEN-LAST:event_formComponentResized

